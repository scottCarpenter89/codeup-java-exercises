package warmups;

public class Stratocaster extends Guitar{

    public Stratocaster(int strings, String pickups, String strap, String inputJack, int frets, String bridge, int volumeKnob, int toneKnobs) {
        super(strings, pickups, strap, inputJack, frets, bridge, volumeKnob, toneKnobs);
    }

    @Override
    public int getStrings() {
        return super.getStrings();
    }

    @Override
    public void setStrings(int strings) {
        super.setStrings(strings);
    }

    @Override
    public String getPickups() {
        return super.getPickups();
    }

    @Override
    public void setPickups(String pickups) {
        super.setPickups(pickups);
    }

    @Override
    public String getStrap() {
        return super.getStrap();
    }

    @Override
    public void setStrap(String strap) {
        super.setStrap(strap);
    }

    @Override
    public String getInputJack() {
        return super.getInputJack();
    }

    @Override
    public void setInputJack(String inputJack) {
        super.setInputJack(inputJack);
    }

    @Override
    public int getFrets() {
        return super.getFrets();
    }

    @Override
    public void setFrets(int frets) {
        super.setFrets(frets);
    }

    @Override
    public String getBridge() {
        return super.getBridge();
    }

    @Override
    public void setBridge(String bridge) {
        super.setBridge(bridge);
    }

    @Override
    public int getVolumeKnob() {
        return super.getVolumeKnob();
    }

    @Override
    public void setVolumeKnob(int volumeKnob) {
        super.setVolumeKnob(volumeKnob);
    }

    @Override
    public int getToneKnobs() {
        return super.getToneKnobs();
    }

    @Override
    public void setToneKnobs(int toneKnobs) {
        super.setToneKnobs(toneKnobs);
    }
}
