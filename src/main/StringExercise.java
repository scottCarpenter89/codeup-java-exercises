package main;

public class StringExercise {
    public static void main (String[] args) {
        String brickInTheWall = "We don't need no education\nWe don't need no thought control\n";
        System.out.println(brickInTheWall);

        String quoteStuff = "Check \"this\" out!, \"s inside of \"s!\n";
        System.out.println(quoteStuff);

        String mainDrive = "In windows, the main drive is usually C:\\\n";
        System.out.println(mainDrive);

        String slashFun = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!\n";
        System.out.println(slashFun);
    }
}
